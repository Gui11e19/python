#Operaciones numericas

x = 4
y = 2
z = 7

print(f"{x} mas {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} por {y} es igual a {x*y}")
print(f"{x} dividido {y} es igual a {x/y}")
print(f"{z} dividido {y} es igual a {z/y}")


#division al piso
print(f"{z} dividido al piso de {y} es igual a {z//y}")

#modulo residuo
print(f"{z} dividido al piso de {y} es igual a {z%y}")

#exponencial
print(f"{x} dividido al piso de {y} es igual a {x**y}")

print(f"{x} dividido al piso de {3} es igual a {x**3}")

#raiz cuadrada
print(f"{x} dividido al piso de {0.5} es igual a {x**0.5}")
